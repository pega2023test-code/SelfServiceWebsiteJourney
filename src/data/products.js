// Build nested map: { jurisdiction → { service → [products] } }
const dataMap = {};
for (const [jurisdiction, service, product] of raw) {
  if (!dataMap[jurisdiction]) dataMap[jurisdiction] = {};
  if (!dataMap[jurisdiction][service]) dataMap[jurisdiction][service] = [];
  dataMap[jurisdiction][service].push(product);
}

export function getServicesForJurisdiction(jurisdictionName) {
  return Object.keys(dataMap[jurisdictionName] || {});
}

export function getProductsForJurisdictionAndService(jurisdictionName, service) {
  return (dataMap[jurisdictionName] && dataMap[jurisdictionName][service]) || [];
}

export default dataMap;
